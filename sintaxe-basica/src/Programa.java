
public class Programa {
	
	public static void main (String[] args){
		
		System.out.println("Ola mundo");
		System.out.println();
		
		//VARIAVEIS: INT
		
		int idade;
		idade = 20;
		
		System.out.println("A idade é " + idade);
		
		//VARIAVEIS: DOUBLE (FLUTUANTE) - PONTOS E VIRGULAS
		
		double salario;
		salario = 1250.70;
		
		System.out.println("O salário é: " + salario);
		
		//CONVERSÃO
		
		int valor = (int) salario;
		
		System.out.println("Conversão: " + valor);
		
		// NAO POSSÍVEL:
		// DOUBLE SALARIO = (SALARIO)
		// INT VALOR = SALARIO
		
		//POSSIVEL:
		//double valor = 3;
		
		//VARIAVEIS: CHAR
		
		char letra ='a';
		
		System.out.println("letra");
		
		//VARIAVEIS: STRING
		
		String frase ="Alura cursos online de tecnologia";
		
		System.out.println(frase);
		
	}
	
}














